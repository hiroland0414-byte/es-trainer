import type { DocType } from "@/domain/types/docType";

export function docTypeImproveTemplates(docType: DocType): string[] {
  if (docType === "self_pr") {
    return [
      "強みが抽象語のままです。強みを1語で言い換えたうえで、“出た場面”を1つに絞って書きましょう。",
      "経験が“根拠”として機能しているか確認。状況→行動→結果（できれば数値）の順で接続しましょう。",
      "再現性が薄いので、同じ強みが別場面でも出る理由（工夫・習慣）を1行で足しましょう。",
    ];
  }
  if (docType === "gakuchika") {
    return [
      "課題が曖昧です。「何が困っていたか」を1文で具体化しましょう。",
      "工夫（あなたの行動）が薄いので、やったことを3点に分けて書きましょう。",
      "結果が弱いので、変化を数字・頻度・比較で表しましょう（例：◯◯が△△→□□）。",
    ];
  }
  // motivation
  return [
    "志望理由が抽象的です。きっかけ→魅力→自分の強み→貢献の順に1本線でつなげましょう。",
    "施設（企業/病院）の特徴と自分の接点が弱いので、見学・HP・実習などの具体要素を1つ入れましょう。",
    "貢献が一般論です。配属先・役割・場面を1つに絞って、何をどう良くするか書きましょう。",
  ];
}

export function docTypeNextTemplates(docType: DocType): string[] {
  if (docType === "self_pr") {
    return [
      "強みを1語で言い換え → それが出たエピソードを1つだけ（状況→行動→結果）で書いてください。",
    ];
  }
  if (docType === "gakuchika") {
    return [
      "課題→工夫→結果 をそれぞれ1文にして、最後に「学び（次に活きる形）」を1文で足してください。",
    ];
  }
  return [
    "施設の特徴（1つ）と、自分の強み（1つ）を線で結び、貢献場面を1つに絞って書き直してください。",
  ];
}